# 3.Найдем элемент в отсортированном массиве, который удовлетворяет условиям:
#   a)Два занятых места находятся в одном ряду;
#   b)Разница между проверяемым местом и предыдущим равна 14.
# 4. Так как надо определить минимальный номер свободного места,
