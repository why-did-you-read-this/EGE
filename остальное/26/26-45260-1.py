f = open('26-45260.txt')
n = int(f.readline())
a = []
otvet = []
for i in f.readlines():
    a.append(list(map(int, i.split())))  # пункт 1
a.sort()  # пункт 2
maxR = 0
minN = 10 ** 20

for i in range(len(a) - 1):  # пункт 3
    if (a[i][0] == a[i + 1][0]) and (a[i + 1][1] - a[i][1] == 14):  # (3.a) and (3.б)
        otvet += [a[i]]

maxR = otvet[-1][0]

for a, b in otvet[::-1]:  # пункт 4
    if a == maxR:
        minN = min(minN, b)
    else:
        break

print(maxR, minN + 1)

